public class House {

	private String ID;
	private String HOURCENO;
	private String HOURCEAREA;
	private String HOURCEADDR;
	private String HOURCERDATE;
	private String HASHNUM;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getHOURCENO() {
		return HOURCENO;
	}
	public void setHOURCENO(String hOURCENO) {
		HOURCENO = hOURCENO;
	}
	public String getHOURCEAREA() {
		return HOURCEAREA;
	}
	public void setHOURCEAREA(String hOURCEAREA) {
		HOURCEAREA = hOURCEAREA;
	}
	public String getHOURCEADDR() {
		return HOURCEADDR;
	}
	public void setHOURCEADDR(String hOURCEADDR) {
		HOURCEADDR = hOURCEADDR;
	}
	public String getHOURCERDATE() {
		return HOURCERDATE;
	}
	public void setHOURCERDATE(String hOURCERDATE) {
		HOURCERDATE = hOURCERDATE;
	}

	public String getHASHNUM() {
		return HASHNUM;
	}
	public void setHASHNUM(String hASHNUM) {
		HASHNUM = hASHNUM;
	}

}
